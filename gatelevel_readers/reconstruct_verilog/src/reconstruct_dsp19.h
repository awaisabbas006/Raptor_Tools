  bool set_param(const std::string &par_name, std::string value) {
    if (parameters.find(par_name) != end(parameters)) {
      parameters[par_name] = value;
      if (par_name == "DSP_MODE") {
        if (value == "MULTIPLY_ACCUMULATE")
          parameters["accumulator"] = "1";
        if (value == "MULTIPLY_ADD_SUB")
          parameters["adder"] = "1";
      } else if (par_name == "OUTPUT_REG_EN") {
        if (value == "TRUE")
          parameters["output_reg"] = "1";
      } else if (par_name == "INPUT_REG_EN") {
        if (value == "TRUE")
          parameters["input_reg"] = "1";
      }
      return true;
    }
    return false;
  }
  std::string get_block_key() {
    return parameters["accumulator"] + parameters["adder"] +
           parameters["output_reg"] + parameters["input_reg"];
  }
  std::string get_MODE_BITS() {
    return parameters["COEFF1_0"] + parameters["COEFF2_0"] +
           parameters["COEFF1_1"] + parameters["COEFF2_1"] +
           parameters["COEFF1_2"] + parameters["COEFF2_2"] +
           parameters["COEFF1_3"] + parameters["COEFF2_3"] +
           std::string("0000") + std::string("1");
  }
  std::unordered_map<std::string, std::string> port_connections;
  void print(std::ostream &ofs) {
    port_connections["$false"] = "$false";
    port_connections["$true"] = "$true";
    port_connections["$undef"] = "$undef";
    std::string rs_prim = RS_DSP_Primitives.at(get_block_key());
    ofs << ".subckt " << rs_prim << " ";
    auto &cons = prim_io_maps[rs_prim];
    std::string known_clock;
    for (auto &cn : cons) {
      if (port_connections.find(cn.second) != end(port_connections)) {
        std::string high_conn = port_connections[cn.second];
        if (cn.first.find("CLK") != std::string::npos)
        {
          if (high_conn != "$undef")
          {
            known_clock = high_conn;
          }
        }
      }
    }
    for (auto &cn : cons) {
      if (port_connections.find(cn.second) != end(port_connections)) {
        ofs << " " << cn.first;
        std::string high_conn = port_connections[cn.second];
        if (cn.first.find("CLK") != std::string::npos)
        {
          if (high_conn == "$undef")
          {
            // We cannot have undriven DSP clock pins or clock pins driven by constants, clock pins have to be driven by clocks
            high_conn = known_clock;
          }
        }
        ofs << "=" << high_conn;
      }
    }
    ofs << std::endl;
    for (auto &outer : port_connections) {
      if (prim_io_maps_dsp_to_rs_prim[rs_prim].find(outer.first) ==
              end(prim_io_maps_dsp_to_rs_prim[rs_prim]) &&
          outer.second != "$false" && outer.second != "$true") {
        // Ports of the wrapper that are not connected in the inner instance
        // should be connected only to constants (No illusion of passing a clck
        // ... )
        std::cerr
            << "CRITICAL WARNING: PRIM_RECONSTRUCT attempt to connect non existant port "
            << outer.first << " to " << outer.second << " in primitive "
            << rs_prim << " Ignored" << std::endl;
      }
    }
    ofs << ".param MODE_BITS " << get_MODE_BITS() << std::endl;
  }
